#!/usr/bin/env node
/**
 * sync-zh-locales.mjs — keep `locales/zh_TW.yml` in sync with the
 * single source of truth `locales/zh_CN.yml`.
 *
 * Chrome's `_locales/` directory does not accept a bare `zh` locale; we
 * must ship `zh_CN` and `zh_TW` as separate folders. To avoid having to
 * maintain two identical Simplified-Chinese files (Cebian intentionally
 * uses Simplified for both Mainland and Taiwan/HK users), this script
 * mirrors `zh_CN.yml` into `zh_TW.yml` on every build.
 *
 * Run it before `wxt prepare`, `wxt`, and `wxt build`.
 */
import { promises as fs } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const SRC = path.join(ROOT, 'locales', 'zh_CN.yml');
const DST = path.join(ROOT, 'locales', 'zh_TW.yml');

const HEADER = `# AUTO-GENERATED from locales/zh_CN.yml by scripts/sync-zh-locales.mjs.
# DO NOT EDIT THIS FILE DIRECTLY. Edit locales/zh_CN.yml and re-run the script.
`;

const src = await fs.readFile(SRC, 'utf8');
await fs.writeFile(DST, HEADER + src, 'utf8');
console.log(`✓ synced locales/zh_CN.yml -> locales/zh_TW.yml`);
